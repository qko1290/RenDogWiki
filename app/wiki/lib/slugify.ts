/**
 * slugify 유틸 함수
 * - 입력받은 문자열을 URL-safe slug로 변환
 * - 한글/영문/숫자/하이픈만 허용 (특수문자 모두 제거)
 * - 공백은 하이픈(-)으로 변환, 앞뒤/연속 하이픈 정리
 * - 위키 heading id, 문서 경로 등에서 활용
 */

export function slugify(text: string): string {
  return text
    .toString()                  // 문자열로 변환(혹시모를 숫자 등 대응)
    .toLowerCase()               // 소문자 통일
    .trim()                      // 앞뒤 공백 제거
    .replace(/\s+/g, '-')        // (1) 모든 공백 -> 하이픈(-) 변환
    .replace(/[^\w\-가-힣]+/g, '')// (2) 영문, 숫자, _, -, 한글만 허용(나머지 제거)
    .replace(/\-\-+/g, '-')      // (3) 연속된 하이픈을 하나로 압축
    .replace(/^-+/, '')          // (4) 앞쪽 하이픈 제거
    .replace(/-+$/, '');         // (5) 뒤쪽 하이픈 제거
}
